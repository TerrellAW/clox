/**
 * Macros to define error codes
 */

#ifndef CLOX_ERROR_H
#define CLOX_ERROR_H

/**
 * Failure to start program
 * Wrong number of arguments
 */
#define START_FAILURE 64

/**
 * Failure to compile
 */
#define COMPILE_FAILURE 65

/**
 * Failure during runtime
 */
#define RUNTIME_FAILURE 70

/**
 * Failure trying to read a file
 */
#define READ_FAILURE 74

#endif
